# Step 2: Preprocess the text (clean punctuation and standardize spaces)
def clean_text(text):
    import re
    text = re.sub(r"[.,;·]", "", text)  # Remove punctuation
    text = re.sub(r"\s+", " ", text).strip()  # Standardize spaces
    return text

