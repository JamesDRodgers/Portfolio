import re #regular expressions used to delineate verses

def split_into_verses(text):
    """
    Split the input text into a dictionary of verse numbers and their corresponding text.
    This function handles text where verses may or may not start on new lines.
    """
    verses = {}  # Dictionary to store verse numbers and text

    # Use a regex pattern to find verse numbers followed by space and text
    verse_pattern = re.compile(r"(\d+)\s([^0-9]+)")

    # Find all matches of the pattern
    matches = verse_pattern.finditer(text)

    for match in matches:
        verse_num = int(match.group(1))  # Extract verse number
        verse_text = match.group(2).strip()  # Extract verse text
        verses[verse_num] = verse_text  # Add to dictionary

    return verses

